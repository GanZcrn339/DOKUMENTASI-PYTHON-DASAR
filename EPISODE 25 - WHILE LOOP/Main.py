# akhir dari program

angka = 0
print(f"angka sekarang -> {angka}")
#ketika di print hasil nya akan menjadi 0

while angka < 5:
	#Jika angka kurang dari 5
	angka += 1
	#maka akan terus di tambah samapai angka <5
	print(f"angka sekarang -> {angka}")
	print("otong ganteng maxsyimaal!")
#lalu kita print, nah bagian print("otong ganteng maxsyimaal!")
#masih masuk ke dalam while maka akan di print juga berkali kali mengikuti sampe var angka kurang dari lima

